n1 = int(input("Enter the number 1 :"))
n2 = int(input("Enter the number 2 :"))

choice = int(input("\n1) addition\n2) subraction\n3) multiplication\n4)division\n Enter your choice : "))

if(choice == 1):
    print(n1 + n2)
elif(choice == 2):
    print(n1 - n2)
elif(choice == 3):
    print(n1 * n2)
else:
    print(n1 / n2)